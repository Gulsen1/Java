package firstpackage;

public class BinaryNumbers {
    public static void main(String[] args) {
        for(int i=0;i<2;i++){
            for(int k=0;k<2;k++){
                for(int m=0; m<2;m++){
                    System.out.println(""+i+k+m);
                }
            }
        }
    }
}
