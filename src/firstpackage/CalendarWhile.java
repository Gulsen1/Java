public class CalendarWhile {
    public static void main(String[] args) {

        int month=1;


        while(month<=12){

            int day=1;  // =that is very important part of the understanding
            while(day<=30){

                System.out.println(month+"/"+day+"/"+2020);
                day++;



            }
          month++;

    }

    }


}


